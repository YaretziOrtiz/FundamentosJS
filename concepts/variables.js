// Variables en Javascripts
// Declaramos variables con let o const
// let para variables que cambiaran de valor
// const para variables que no cambiaran de valor 

let name;
console.log(name); 
name = "Peter";
console.log(name);
console.log(typeof name);
name = 18;
console.log(name);
console.log(typeof name);

const PI = 3.1416;
console.log(PI);
console.log(typeof PI);
