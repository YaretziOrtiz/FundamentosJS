// Operadores Matematematicos
// + y -
// * y /
// % y ** Modulo y potencia

console.log(10 + 3);
console.log(10 - 3);
console.log(10 * 3);
console.log(10 / 3);
console.log(10 % 3);
console.log(10 ** 3);


// Operadores string 
// concatenacion con +
console.log("Hola" + " " + "Mundo");

// Operadores de asignacion
let a = 1;
a += 3;
a -= 3;
a *= 3;
a /= 3;
a %= 3;
a **= 3;
console.log(a); 

// Operadores de comparacion
console.log(10 > 3);
console.log(10 < 3);
console.log(10 >= 3);
console.log(10 <= 3);
console.log(10 == 3);
console.log(10 === 3);
console.log(10 != 3);
console.log(10 !== 3);

// Operadores logicos
console.log(true && true); // And
console.log(true || true); // Or
console.log(!true); // Not